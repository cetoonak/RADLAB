DECLARE @Id INT
DECLARE @Guid VARCHAR(50)

DECLARE C CURSOR LOCAL FOR
SELECT
	IlIlce.Id,
	dbo.FArray(S.X, ';', 1) AS Guid
FROM
	dbo.FStringList(@TurkakIl, CHAR(13) + CHAR(10)) AS S
	LEFT JOIN IlIlce ON REPLACE(dbo.FArray(S.X, ';', 0), CHAR(10), '') = IlIlce.IlIlce

OPEN C

FETCH NEXT FROM C
INTO @Id, @Guid

WHILE (@@FETCH_STATUS <> -1)
BEGIN
	UPDATE IlIlce SET 
		TurkakGuid = @Guid
	WHERE
		Id = @Id

	FETCH NEXT FROM C
	INTO @Id, @Guid
END

CLOSE C
DEALLOCATE C